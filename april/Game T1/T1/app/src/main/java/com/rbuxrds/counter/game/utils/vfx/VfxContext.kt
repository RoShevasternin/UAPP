package com.rbuxrds.counter.game.utils.vfx

// ─────────────────────────────────────────────────────────────────────────────
// VfxContext — інформація про поточний render target
// ─────────────────────────────────────────────────────────────────────────────

/**
 * Контекст що передається в кожен ефект під час render().
 *
 * Навіщо він потрібен: деякі шейдери (наприклад blur) потребують знати фізичний
 * розмір буфера в пікселях щоб правильно обчислити зміщення між сусідніми
 * текселями (u_groupSize uniform). Без цього blur буде або надто слабким
 * або надто сильним залежно від розміру актора.
 *
 * width/height — розміри в логічних stage-одиницях (2160×3840 система)
 * bufferW/bufferH — фізичні пікселі FBO (залежать від viewport scaling)
 */
data class VfxContext(
    val width  : Float,
    val height : Float,
    val bufferW: Int,
    val bufferH: Int,
)