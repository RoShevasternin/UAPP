package com.robuxe.robuxtracker.freerobux.adsmodule.model;

public class Admob {

    private String app_open_ads_id;
    private String banner_ads_id;
    private String int_ads_id;
    private String rewarded_ads_id;
    private String native_ads_id;

    public String getApp_open_ads_id() {
        return app_open_ads_id;
    }

    public void setApp_open_ads_id(String value) {
        this.app_open_ads_id = value;
    }

    public String getBanner_ads_id() {
        return banner_ads_id;
    }

    public void setBanner_ads_id(String value) {
        this.banner_ads_id = value;
    }

    public String getInt_ads_id() {
        return int_ads_id;
    }

    public void setInt_ads_id(String value) {
        this.int_ads_id = value;
    }

    public String getRewarded_ads_id() {
        return rewarded_ads_id;
    }

    public void setRewarded_ads_id(String value) {
        this.rewarded_ads_id = value;
    }

    public String getNative_ads_id() {
        return native_ads_id;
    }

    public void setNative_ads_id(String value) {
        this.native_ads_id = value;
    }
}
